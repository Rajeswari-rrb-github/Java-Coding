public class SwitchCases {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1: 
            System.out.println("samosa");
                break;
            case 2 : System.out.println("momo");
                break;
            case 3: System.out.println("chowmin");
                break;
            default: System.out.println("We realize we're dreaming");
        }

        char food = 'f';
        switch (food) {
            case 'a': 
            System.out.println("pizza");
                break;
            case 'f' : System.out.println("choco");
                break;
            case 'd': System.out.println("alooposto");
                break;
            default: System.out.println("We realize we're dreaming");
        }
    }
}
