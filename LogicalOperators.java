public class LogicalOperators{
    // logical AND
    public static void main(String[] args, int i) {
        System.out.println(3>2 && 5<9);
        System.out.println(3==2);
        System.out.println(3>2);
        System.out.println(3==2 || 5<0);
        System.out.println(! (3>2));
        System.out.println(! (3==2));

    }
}
