public class CharacterDataType_increment {
    public static void main(String[] args) {
        char ch1, ch2;
        ch1= 88;
        ch2= 'Y';
        System.out.print("ch1 and ch2 are : ");
        System.out.println(ch1 + " " + ch2);
        ch1++;
        ch2++;
        System.out.print("Now ch1 and ch2 after increment are : ");
        System.out.print(ch1 + " " + ch2);
    }
}
